#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	char	*p;
	size_t	i;

	i = 0;
	p = (char *)s;
	while (i < n)
	{
		if (*(p + i) == (char)c)
			return (p + i);
		i++;
	}
	return (0);
}
