#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	unsigned int	i;
	char			c;

	i = 0;
	c = '\n';
	if (!s)
		return ;
	while (s[i])
	{
		write(fd, &s[i], 1);
		i++;
	}
	write(fd, &c, 1);
	return ;
}
