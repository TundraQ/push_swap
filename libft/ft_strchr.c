#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*p;

	p = (char *)s;
	while (*p)
	{
		if (*p == (char) c)
			return (p);
		p = p + 1;
	}
	if (c == '\0')
		return (p);
	return (NULL);
}
